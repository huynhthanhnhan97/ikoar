const setProjectionMatrix = (projectionMatrix, value) => {
    let array = [];
    for (let key in value) {
        array[key] = value[key];
    }
    if (typeof projectionMatrix.elements.set === "function") {
        projectionMatrix.elements.set(array);
    } else {
        projectionMatrix.elements = [].slice.call(array);
    }
};

